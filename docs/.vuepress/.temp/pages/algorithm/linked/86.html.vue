<template><h1 id="_86-分隔链表" tabindex="-1"><a class="header-anchor" href="#_86-分隔链表" aria-hidden="true">#</a> 86. 分隔链表</h1>
<p>给定一个链表和一个特定值 x，对链表进行分隔，使得所有小于 x 的节点都在大于或等于 x 的节点之前。</p>
<p>你应当保留两个分区中每个节点的初始相对位置。</p>
<p></p>
<p>示例:</p>
<p>输入: head = 1-&gt;4-&gt;3-&gt;2-&gt;5-&gt;2, x = 3
输出: 1-&gt;2-&gt;2-&gt;4-&gt;3-&gt;5</p>
</template>
