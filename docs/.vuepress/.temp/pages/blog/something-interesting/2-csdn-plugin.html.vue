<h3 id="_2-1-一键万能框搜索" tabindex="-1"><a class="header-anchor" href="#_2-1-一键万能框搜索" aria-hidden="true">#</a> 2.1 一键万能框搜索</h3>
<p>任意界面下，都按 C 一键搜索，还能搜到书签历史记录哦</p>
<ol>
<li>按字母“C”呼出万能框
<ul>
<li>在万能框输不同的短口令，即可实现一键 JSON 转换 、一键转换</li>
</ul>
</li>
<li>一键极简搜索
<ul>
<li>任意网页下，键盘按下字母 『C』，即可弹出万能搜索框，一键搜索全网内容+本地书签+历史记录，大幅提升搜索效率。</li>
</ul>
</li>
<li>更多快捷键：
<ul>
<li>键盘按下 “b” : 仅搜索书签 ； 键盘按下 shift+&quot;t&quot; ，切换标签页</li>
</ul>
</li>
</ol>
