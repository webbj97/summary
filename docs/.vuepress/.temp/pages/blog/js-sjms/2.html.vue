一个基于策略模式的程序至少由两部分组成。第一个部分是一组策略类，策略类封装了具体
的算法，并负责具体的计算过程。第二个部分是环境类 Context，Context 接受客户的请求，随后
把请求委托给某一个策略类。要做到这点，说明 Context 中要维持对某个策略对象的引用。
