<p>定义：定义一系列的算法，把他们封装起来，并且可以使它们相互替换。</p>
<p>不变的部分和变化的部分隔开是每个设计模式的主题</p>
<p>策略模式也不例外，策略模式的目的就是将算法的使用与算法的实现分离开来。
