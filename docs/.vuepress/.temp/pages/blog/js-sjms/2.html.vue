<h2 id="二、策略模式的优缺点" tabindex="-1"><a class="header-anchor" href="#二、策略模式的优缺点" aria-hidden="true">#</a> 二、策略模式的优缺点</h2>
<p><strong>优点：</strong></p>
<ol>
<li>策略模式利用组合、委托和多态等技术和思想，可以有效的避免多重条件选择语句</li>
<li>策略模式提供了对 开放-封闭 原则完美的支持，易于切换、易于理解、易于扩展</li>
<li>策略模式中的部分算法，也可以服用到其他地方</li>
</ol>
<p><strong>缺点：</strong></p>
<ol>
<li>策略模式会在程序中增加许多策略类，和策略对象</li>
<li>每一种策略都是要我们掌握的，也导致它们报漏在外</li>
</ol>
